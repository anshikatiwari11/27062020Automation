//Write a for loop to find the sum of first 100 numbers(1 to 100).
package assignment1Programs07072020;

public class Problem4 {

	public static void main(String[] args) {
		int i;
		int sum =0;
		for(i=1; i<=100; i++)
		{
			sum = sum+i;
			{
				System.out.println(sum);
			}
		}
	}

}
